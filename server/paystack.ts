// ---------------------------------------------------------------------------
// Generic Paystack API response wrapper
// ---------------------------------------------------------------------------
interface PaystackApiResponse<T> {
  status: boolean;
  message: string;
  data: T;
}

// ---------------------------------------------------------------------------
// Internal fetch helper — every call to Paystack goes through here
// ---------------------------------------------------------------------------
class PaystackError extends Error {
  constructor(
    message: string,
    public readonly statusCode?: number,
    public readonly paystackMessage?: string,
  ) {
    super(message);
    this.name = "PaystackError";
  }
}

async function paystackFetch<T>(
  path: string,
  options: Omit<RequestInit, "headers"> & { headers?: Record<string, string> } = {},
): Promise<T> {
  const { secretKey } = getConfig();
  const url = `${PAYSTACK_BASE}${path}`;

  const headers: Record<string, string> = {
    Authorization: `Bearer ${secretKey}`,
    "Content-Type": "application/json",
    ...(options.headers ?? {}),
  };

  let response: Response;
  try {
    response = await fetch(url, { ...options, headers });
  } catch (err: unknown) {
    throw new PaystackError(
      `Network error reaching Paystack: ${err instanceof Error ? err.message : String(err)}`,
    );
  }

  // Paystack may return an empty 200 for some DELETE / disable actions
  const bodyText = await response.text();
  let body: PaystackApiResponse<T>;
  if (bodyText) {
    try {
      body = JSON.parse(bodyText) as PaystackApiResponse<T>;
    } catch {
      throw new PaystackError(
        `Invalid JSON response from Paystack (HTTP ${response.status})`,
        response.status,
      );
    }
  } else {
    // Empty body — construct a minimal success shape
    body = { status: true, message: "OK", data: null as unknown as T };
  }

  if (!response.ok || !body.status) {
    throw new PaystackError(
      body.message || `Paystack API returned HTTP ${response.status}`,
      response.status,
      body.message,
    );
  }

  return body.data;
}

