export type PaystackPlanInterval = "hourly" | "daily" | "weekly" | "monthly" | "quarterly" | "biannually" | "annually";

export interface CreatePlanRequest {
  /** Plan name shown to the customer */
  name: string;
  /** Amount in ZAR cents */
  amount: number;
  /** Billing interval */
  interval: PaystackPlanInterval;
  /** Optional description */
  description?: string;
  /** Optional invoice limit (number of invoices before expiry) */
  invoiceLimit?: number;
  /** Whether to send invoices via email */
  sendInvoices?: boolean;
  /** Whether to send SMS notifications */
  sendSms?: boolean;
  /** Currency (defaults to ZAR) */
  currency?: string;
}

export interface CreatePlanData {
  id: number;
  name: string;
  plan_code: string;
  description: string | null;
  amount: number;
  interval: PaystackPlanInterval;
  send_invoices: boolean;
  send_sms: boolean;
  currency: string;
  invoice_limit: number;
  createdAt: string;
  updatedAt: string;
}

/**
 * Create a Paystack subscription plan.
 *
 * Mystic AI plans:
 *   - "Mystic Monthly"  — 9900  (R99.00 monthly)
 *   - "Mystic Annual"   — 79900 (R799.00 annually)
 *
 * @returns Created plan data including the plan_code needed for subscriptions
 */
export async function createPaystackPlan(
  params: CreatePlanRequest,
): Promise<CreatePlanData> {
  const payload: Record<string, unknown> = {
    name: params.name,
    amount: Math.round(params.amount),
    interval: params.interval,
    currency: params.currency || "ZAR",
  };

  if (params.description) {
    payload.description = params.description;
  }
  if (params.invoiceLimit !== undefined) {
    payload.invoice_limit = params.invoiceLimit;
  }
  if (params.sendInvoices !== undefined) {
    payload.send_invoices = params.sendInvoices;
  }
  if (params.sendSms !== undefined) {
    payload.send_sms = params.sendSms;
  }

  return paystackFetch<CreatePlanData>("/plan", {
    method: "POST",
    body: JSON.stringify(payload),
  });
}

