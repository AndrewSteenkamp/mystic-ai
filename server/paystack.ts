// 1. INITIALIZE TRANSACTION
// ===========================================================================

export interface InitializeTransactionRequest {
  /** Amount in ZAR (Paystack expects kobo for NGN, but for ZAR it's the base unit — cents/100) */
  amount: number;
  email: string;
  /** URL to redirect to after payment (success / cancel handled by Paystack inline) */
  callbackUrl?: string;
  /** Optional custom reference. Auto-generated by Paystack if omitted. */
  reference?: string;
  /** Arbitrary metadata attached to the transaction */
  metadata?: Record<string, unknown>;
  /** Payment channels to restrict (e.g. ["card", "bank"]) */
  channels?: string[];
}

export interface InitializeTransactionData {
  authorization_url: string;
  access_code: string;
  reference: string;
}

/**
 * Initialize a Paystack transaction and return the authorization URL + reference.
 *
 * @param amount - Amount in ZAR cents (e.g. R29.00 = 2900, R99.00 = 9900)
 * @param email  - Customer email address
 * @param callbackUrl - URL to redirect after payment
 * @returns { authorization_url, access_code, reference }
 */
export async function initializePaystackTransaction(
  amount: number,
  email: string,
  callbackUrl?: string,
  metadata?: Record<string, unknown>,
): Promise<InitializeTransactionData> {
  const payload: Record<string, unknown> = {
    amount: Math.round(amount), // ensure integer cents
    email,
  };

  if (callbackUrl) {
    payload.callback_url = callbackUrl;
  }
  if (metadata) {
    payload.metadata = metadata;
  }

  return paystackFetch<InitializeTransactionData>("/transaction/initialize", {
    method: "POST",
    body: JSON.stringify(payload),
  });
}

