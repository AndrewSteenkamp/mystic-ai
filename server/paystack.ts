export type PaystackWebhookEventType =
  | "charge.success"
  | "charge.failed"
  | "charge.dispute.create"
  | "charge.dispute.remind"
  | "subscription.create"
  | "subscription.disable"
  | "subscription.expiring_cards"
  | "subscription.not_renew"
  | "transfer.success"
  | "transfer.failed"
  | "transfer.reversed";

export interface PaystackWebhookEvent {
  event: PaystackWebhookEventType;
  data: VerifyTransactionData | Record<string, unknown>;
}

export interface WebhookProcessingResult {
  acknowledged: boolean;
  event: PaystackWebhookEventType;
  reference?: string;
  action: "processed" | "ignored" | "error";
  message: string;
}

/**
 * Validate the Paystack webhook signature (HMAC SHA-512).
 *
 * Paystack signs every webhook payload with your secret key.
 * The signature is sent in the `x-paystack-signature` HTTP header.
 *
 * @param rawBody  - The raw request body as a string (exactly as received)
 * @param signature - The value of the `x-paystack-signature` header
 * @returns true if the signature is valid
 */
export function validatePaystackWebhookSignature(
  rawBody: string,
  signature: string,
): boolean {
  const { secretKey } = getConfig();
  const computed = crypto
    .createHmac("sha512", secretKey)
    .update(rawBody)
    .digest("hex");
  return crypto.timingSafeEqual(
    Buffer.from(computed, "utf-8"),
    Buffer.from(signature, "utf-8"),
  );
}

/**
 * Process a Paystack webhook event.
 *
 * Handles:
 *   - charge.success       → payment completed for a single reading
 *   - subscription.create  → new recurring subscription activated
 *   - subscription.disable → subscription cancelled / expired
 *
 * All other events are acknowledged but ignored.
 *
 * @param rawBody   - Raw request body string (needed for signature validation)
 * @param signature - x-paystack-signature header value
 * @returns Processing result
 */
export async function handlePaystackWebhook(
  rawBody: string,
  signature: string,
): Promise<WebhookProcessingResult> {
  // --- 1. Validate signature ---
  if (!validatePaystackWebhookSignature(rawBody, signature)) {
    return {
      acknowledged: false,
      event: "charge.success" as PaystackWebhookEventType, // placeholder
      action: "error",
      message: "Invalid webhook signature",
    };
  }

  // --- 2. Parse the event ---
  let event: PaystackWebhookEvent;
  try {
    event = JSON.parse(rawBody) as PaystackWebhookEvent;
  } catch {
    return {
      acknowledged: false,
      event: "charge.success" as PaystackWebhookEventType,
      action: "error",
      message: "Failed to parse webhook body as JSON",
    };
  }

  const eventType = event.event;
  const reference =
    (event.data as VerifyTransactionData)?.reference ?? undefined;

  // --- 3. Route by event type ---
  switch (eventType) {
    case "charge.success": {
      // Single payment succeeded — e.g. a R29 reading was paid
      // TODO: link to your business logic (grant access, send email, etc.)
      return {
        acknowledged: true,
        event: eventType,
        reference,
        action: "processed",
        message: `Payment ${reference} succeeded`,
      };
    }

    case "subscription.create": {
      // New subscription created — R99 monthly or R799 annual
      return {
        acknowledged: true,
        event: eventType,
        reference,
        action: "processed",
        message: `Subscription created for ${reference}`,
      };
    }

    case "subscription.disable": {
      // Subscription disabled (cancelled, expired, non-payment)
      return {
        acknowledged: true,
        event: eventType,
        reference,
        action: "processed",
        message: `Subscription disabled for ${reference}`,
      };
    }

    default: {
      // Acknowledge but ignore other event types
      return {
        acknowledged: true,
        event: eventType,
        reference,
        action: "ignored",
        message: `Event type '${eventType}' acknowledged but not processed`,
      };
    }
  }
}

